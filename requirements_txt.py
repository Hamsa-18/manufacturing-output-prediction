import streamlit as st
import joblib
import numpy as np
import pandas as pd

# Load model and scaler
model = joblib.load("linear_regression_model.pkl")
scaler = joblib.load("scaler.pkl")

st.title("üè≠ Manufacturing Equipment Output Prediction")
st.write("Predict hourly machine output based on input parameters")

# Input fields
temperature = st.number_input("Injection Temperature", value=200)
pressure = st.number_input("Injection Pressure", value=1500)
cycle_time = st.number_input("Cycle Time", value=30)
cooling_time = st.number_input("Cooling Time", value=10)
viscosity = st.number_input("Material Viscosity", value=0.8)
ambient_temp = st.number_input("Ambient Temperature", value=25)
machine_age = st.number_input("Machine Age (years)", value=5)
operator_exp = st.number_input("Operator Experience (years)", value=2)
maint_hours = st.number_input("Maintenance Hours", value=3)
eff_score = st.number_input("Efficiency Score", value=80)
machine_util = st.number_input("Machine Utilization (%)", value=75)

# Convert inputs to DataFrame
input_data = pd.DataFrame([[temperature, pressure, cycle_time, cooling_time,
                            viscosity, ambient_temp, machine_age, operator_exp,
                            maint_hours, eff_score, machine_util]],
                          columns=['Injection_Temperature', 'Injection_Pressure',
                                   'Cycle_Time', 'Cooling_Time', 'Material_Viscosity',
                                   'Ambient_Temperature', 'Machine_Age',
                                   'Operator_Experience', 'Maintenance_Hours',
                                   'Efficiency_Score', 'Machine_Utilization'])

# Scale input
scaled_data = scaler.transform(input_data)

# Predict
prediction = model.predict(scaled_data)

st.success(f"Predicted Parts per Hour: {prediction[0]:.2f}")

